// Generates the native Android string resources from the SAME i18next locale
// files the web app uses (public/app/locales/<lng>/translation.json).
//
// Why this exists: the Android shell renders a few strings natively — Toasts in
// MainActivity.kt, the loading-screen contentDescription — that the WebView
// never draws, so they cannot come from i18next at runtime. Rather than
// hand-maintain a parallel strings.xml (which drifts from the web copy and never
// gets translated), this script treats the string resources as a DERIVED
// artifact of the one translation store: it reads each locale's JSON and emits
// res/values*/strings_generated.xml.
//
// Source of truth for *which* strings the shell needs — and their Android
// resource names — is the ANDROID_STRINGS map below. Each entry maps an Android
// resource name to a translation key; the key must exist in the `en` file (the
// default/fallback locale) or this script throws, so wiring up a new native
// string forces a matching translation. Localized files fall back to `en`
// per key, matching i18next's `fallbackLng: "en"`.
//
// The generated files carry a DO NOT EDIT header. Hand-authored, non-translatable
// config (package_name, custom_url_scheme, …) stays in strings.xml; Android
// merges every values/*.xml, so the two coexist.
//
// app_name / title_activity_main are generated rather than config: the brand is
// written "تصفير" in Arabic, so the launcher label is translatable copy. Android
// picks it by DEVICE locale, so an Arabic phone shows تصفير on the home screen
// regardless of the language chosen inside the app.
//
// The same LOCALES list also emits res/xml/locales_config.xml, which is what
// makes the app appear under Android 13+ Settings → Apps → Language. Deriving it
// here means adding a locale can't leave the per-app language picker behind.
//
// Run via `npm run gen:android-strings`. Also run by scripts/build-and-sync.sh —
// the Gradle preBuild hook every real Android build (including CI releases)
// goes through — and by the cap:sync convenience scripts.

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(here, "..");
const repoRoot = resolve(webRoot, "..", "..");

const LOCALES_DIR = join(webRoot, "public", "app", "locales");
const ANDROID_RES = join(
  repoRoot,
  "apps",
  "android",
  "app",
  "src",
  "main",
  "res",
);

// Android resource name -> translation key. Adding a native string means adding
// an entry here and the key to translation.json. `loading` reuses the shared
// `common.loading` key; camera strings are native-only, kept under `native.*`.
const ANDROID_STRINGS = {
  app_name: "brand.name",
  title_activity_main: "brand.name",
  loading: "common.loading",
  camera_permission_required: "native.cameraPermissionRequired",
  camera_open_failed: "native.cameraOpenFailed",
  image_process_failed: "native.imageProcessFailed",
  clipboard_copied_text: "native.clipboardCopiedText",
  clipboard_cut_text: "native.clipboardCutText",
};

// Locale -> res/values* directory. The first entry is the default/fallback that
// every other locale falls back to per key.
const LOCALES = [
  { lng: "en", valuesDir: "values" },
  { lng: "ar", valuesDir: "values-ar" },
];
const FALLBACK_LNG = LOCALES[0].lng;

function loadLocale(lng) {
  const file = join(LOCALES_DIR, lng, "translation.json");
  return JSON.parse(readFileSync(file, "utf8"));
}

// Escape a string for use as an Android <string> value. XML entities first, then
// Android's own apostrophe/quote backslash-escapes, then a leading @/? which
// Android would otherwise read as a resource/attr reference.
function escapeAndroid(value) {
  let out = value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/'/g, "\\'")
    .replace(/"/g, '\\"');
  if (out.startsWith("@") || out.startsWith("?")) out = "\\" + out;
  return out;
}

function renderXml(lng, resources) {
  const lines = resources
    .map(({ name, value }) => `    <string name="${name}">${value}</string>`)
    .join("\n");
  return (
    `<?xml version="1.0" encoding="utf-8"?>\n` +
    `<!-- DO NOT EDIT. Generated by scripts/gen-android-strings.mjs from\n` +
    `     public/app/locales/${lng}/translation.json. Run \`npm run gen:android-strings\`. -->\n` +
    `<resources>\n${lines}\n</resources>\n`
  );
}

// Skip the write when content is unchanged: this runs on every native build
// (build-and-sync.sh), and a fresh mtime would re-trigger Gradle's resource
// merge each time.
function writeIfChanged(path, content) {
  try {
    if (readFileSync(path, "utf8") === content) return;
  } catch {
    // Missing file — write it.
  }
  writeFileSync(path, content);
}

// res/xml/locales_config.xml — the platform's list of languages this app ships,
// used by the Android 13+ per-app language picker. The fallback locale goes
// first as the default.
function renderLocalesConfig() {
  const items = LOCALES.map(
    ({ lng }) => `    <locale android:name="${lng}"/>`,
  ).join("\n");
  return (
    `<?xml version="1.0" encoding="utf-8"?>\n` +
    `<!-- DO NOT EDIT. Generated by scripts/gen-android-strings.mjs from the\n` +
    `     LOCALES list. Run \`npm run gen:android-strings\`. -->\n` +
    `<locale-config xmlns:android="http://schemas.android.com/apk/res/android">\n` +
    `${items}\n</locale-config>\n`
  );
}

function main() {
  const base = loadLocale(FALLBACK_LNG);

  // Every mapped key must resolve in the fallback locale, or the resource would
  // be absent and R.string.<name> would fail to compile on the native side.
  const missing = Object.entries(ANDROID_STRINGS).filter(
    ([, key]) => typeof base[key] !== "string",
  );
  if (missing.length > 0) {
    throw new Error(
      `Translation key(s) missing from ${FALLBACK_LNG}/translation.json for ` +
        `gen-android-strings.mjs:\n` +
        missing.map(([name, key]) => `  - ${name} -> ${key}`).join("\n") +
        `\nAdd each key to the locale files (at least ${FALLBACK_LNG}).`,
    );
  }

  for (const { lng, valuesDir } of LOCALES) {
    const table = lng === FALLBACK_LNG ? base : loadLocale(lng);
    const resources = Object.entries(ANDROID_STRINGS).map(([name, key]) => {
      const raw = typeof table[key] === "string" ? table[key] : base[key];
      if (lng !== FALLBACK_LNG && typeof table[key] !== "string") {
        console.warn(
          `gen-android-strings: ${lng} missing "${key}", ` +
            `falling back to ${FALLBACK_LNG} for <string name="${name}">.`,
        );
      }
      return { name, value: escapeAndroid(raw) };
    });

    const out = join(ANDROID_RES, valuesDir, "strings_generated.xml");
    mkdirSync(dirname(out), { recursive: true });
    writeIfChanged(out, renderXml(lng, resources));
    console.log(
      `gen-android-strings: wrote ${resources.length} string(s) to ` +
        `res/${valuesDir}/strings_generated.xml (${lng}).`,
    );
  }

  const localesConfig = join(ANDROID_RES, "xml", "locales_config.xml");
  mkdirSync(dirname(localesConfig), { recursive: true });
  writeIfChanged(localesConfig, renderLocalesConfig());
  console.log(
    `gen-android-strings: wrote ${LOCALES.length} locale(s) to ` +
      `res/xml/locales_config.xml.`,
  );
}

main();
