// Generates the Electron main-process strings from the SAME i18next locale
// files the web app uses (public/app/locales/<lng>/translation.json).
//
// Why this exists: the macOS application menu and the tray menu are built in the
// main process, which has no DOM, no i18next and no access to the renderer's
// language. Those labels are drawn by the OS, so they cannot come from i18next
// at runtime. Rather than hand-maintain English literals in `main/index.ts`,
// this script treats them as a DERIVED artifact of the one translation store and
// emits `apps/desktop/src/main/strings.generated.ts`.
//
// Source of truth for *which* strings the main process needs is DESKTOP_STRINGS
// below. Each key must exist in the source locale or this script throws, so
// wiring up a new menu label forces a matching translation. Localized values
// fall back to the source locale per key, matching i18next's `fallbackLng: "en"`.
//
// The renderer-drawn Windows/Linux menu bar (`ElectronMenuBar.tsx`) reads the
// same `menu.*` keys through i18next directly — it lives in the WebView — so the
// two menus stay in step by construction.
//
// Run via `npm run gen:desktop-strings`; the desktop app's own `build` script
// invokes it too, so a packaged build can never ship stale labels.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(here, "..");
const repoRoot = resolve(webRoot, "..", "..");

const LOCALES_DIR = join(webRoot, "public", "app", "locales");
const OUT = join(repoRoot, "apps", "desktop", "src", "main", "strings.generated.ts");

// Translation keys the main process draws. Keep in sync with the labels used in
// `buildMacMenu` and `createTray`.
const DESKTOP_STRINGS = [
  "menu.view",
  "menu.showInspector",
  "menu.showTasfer",
  "menu.quit",
];

// Locales to emit. The first is the source language and the per-key fallback for
// every other locale.
const LOCALES = ["en"];
const SOURCE_LNG = LOCALES[0];

function loadLocale(lng) {
  return JSON.parse(
    readFileSync(join(LOCALES_DIR, lng, "translation.json"), "utf8"),
  );
}

function main() {
  const tables = Object.fromEntries(LOCALES.map((lng) => [lng, loadLocale(lng)]));
  const base = tables[SOURCE_LNG];

  const missing = DESKTOP_STRINGS.filter((key) => typeof base[key] !== "string");
  if (missing.length > 0) {
    throw new Error(
      `Translation key(s) missing from ${SOURCE_LNG}/translation.json for ` +
        `gen-desktop-strings.mjs:\n` +
        missing.map((key) => `  - ${key}`).join("\n") +
        `\nAdd each key to the locale files (at least ${SOURCE_LNG}).`,
    );
  }

  const entries = LOCALES.map((lng) => {
    const table = tables[lng];
    const rows = DESKTOP_STRINGS.map((key) => {
      const value = typeof table[key] === "string" ? table[key] : base[key];
      if (lng !== SOURCE_LNG && typeof table[key] !== "string") {
        console.warn(
          `gen-desktop-strings: ${lng} missing "${key}", ` +
            `falling back to ${SOURCE_LNG}.`,
        );
      }
      return `    ${JSON.stringify(key)}: ${JSON.stringify(value)},`;
    }).join("\n");
    return `  ${JSON.stringify(lng)}: {\n${rows}\n  },`;
  }).join("\n");

  const keyUnion = DESKTOP_STRINGS.map((k) => JSON.stringify(k)).join(" | ");

  const out =
    `// DO NOT EDIT. Generated by apps/web/scripts/gen-desktop-strings.mjs from\n` +
    `// apps/web/public/app/locales/<lng>/translation.json.\n` +
    `// Run \`npm run gen:desktop-strings\` in apps/web.\n\n` +
    `export const LOCALES = [${LOCALES.map((l) => JSON.stringify(l)).join(", ")}] as const;\n\n` +
    `export type Locale = (typeof LOCALES)[number];\n\n` +
    `export type StringKey = ${keyUnion};\n\n` +
    `/** Per-key fallback locale, matching i18next's \`fallbackLng\`. */\n` +
    `export const FALLBACK_LOCALE: Locale = ${JSON.stringify(SOURCE_LNG)};\n\n` +
    `export const STRINGS: Record<Locale, Record<StringKey, string>> = {\n` +
    `${entries}\n};\n`;

  writeFileSync(OUT, out);
  console.log(
    `gen-desktop-strings: wrote ${DESKTOP_STRINGS.length} string(s) x ` +
      `${LOCALES.length} locale(s) to apps/desktop/src/main/strings.generated.ts.`,
  );
}

main();
